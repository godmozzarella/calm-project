package com.calm.feature.user;

/**
 * Роль пользователя. По умолчанию все регистрируются как USER.
 * Первый ADMIN определяется через env {@code CALM_ADMIN_EMAIL} —
 * аккаунт с этим email получает роль автоматически при регистрации/логине.
 * Последующих админов назначает действующий админ через UI.
 */
public enum Role {
	USER,
	ADMIN
}
